    private void editarCampo(String campo, String valor, String codigo)
    {
       count++;
       controlUS.procesarEdicionUsuario(campo, valor, codigo, count); 
    }
    
    private void jButtonGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonGuardarMouseClicked
        count =0;
        
        String codigo = jTextFieldBuscar.getText();
  
        editarCampo("nombre", jTFNombre.getText(), codigo);
        
        editarCampo("docid", jTFCC.getText(), codigo);

        editarCampo("nombreusuario", jTFNombreU.getText(), codigo);
        
        editarCampo("contrasenha", jTFContra.getText(), codigo);
        
        editarCampo("fecharegistro", jTFFecha.getText(), codigo);
        
        editarCampo("direccion", jTFDireccion.getText(), codigo);
        
        editarCampo("telefono", jTFTelefono.getText(), codigo);
        
        editarCampo("cargo", jTFCargo.getText(), codigo);
        
        
    }//GEN-LAST:event_jButtonGuardarMouseClicked

