    private void jButtonRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonRegistrarMouseClicked
        
        Usuario miUsuario = new Usuario();
        
        miUsuario.setCodigo(jTextFieldCodigo.getText());
        miUsuario.setDocId(jTextFieldCC.getText());
        miUsuario.setNombre(jTextFieldNombre.getText());
        miUsuario.setCargo(jTextFieldCargo.getText());
        miUsuario.setNombreUsuario(jTextFieldNomUser.getText());
        miUsuario.setContrasenha(jTextFieldPassword.getText());
        miUsuario.setEstado("Activo");
        miUsuario.setFechaRegistro(jTextFiledFecha.getText());
        miUsuario.setDireccion(jTextFieldDireccion.getText());
        miUsuario.setTelefono(jTextFieldTelefono.getText());
        
        ConexionBaseDatos conexionBD = new ConexionBaseDatos();
        conexionBD.insertarUsuario(miUsuario);
        compRegistro(conexionBD.getDatoEncontrado());
    }//GEN-LAST:event_jButtonRegistrarMouseClicked

       private void compRegistro(boolean comp)
    {
        if(comp)
        {
            VentanaMRegistroExit  vMRE= new VentanaMRegistroExit();
            vMRE.setVisible(true);
        }else
        {
            VentanaMRegistroNExit  vMRNE= new VentanaMRegistroNExit();
            vMRNE.setVisible(true);
        }
    }
