    private void jBotonBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBotonBuscarMouseClicked
        ConexionBaseDatos conexionBD = new ConexionBaseDatos();
        
       Usuario miUsuario = conexionBD.consultarUsuario(jTextFieldBuscar.getText());
       if(conexionBD.getDatoEncontrado())
       { 
            jLabelRNombre.setText(miUsuario.getNombre());
            jLabelRCC.setText(miUsuario.getDocId());
            jLabelRNUsuario.setText(miUsuario.getNombreUsuario());
            jLabelRPassword.setText(miUsuario.getContrasenha());
            jLabelRFecha.setText(miUsuario.getFechaRegistro());
            jLabelRCargo.setText(miUsuario.getCargo());
       }else
       {
           VentanaMensajeError mensajeError = new VentanaMensajeError();
           mensajeError.setVisible(true);
            jLabelRNombre.setText("");
            jLabelRCC.setText("");
            jLabelRNUsuario.setText("");
            jLabelRPassword.setText("");
            jLabelRFecha.setText("");
            jLabelRCargo.setText("");
       }
        
    }//GEN-LAST:event_jBotonBuscarMouseClicked
    
